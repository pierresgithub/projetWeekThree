    <h2>nutrition</h2>
    <div id=hummusnutrition>

     <p>Chickpeas, the main ingredient of conventional hummus, have appreciable contents of dietary fiber, protein, B vitamins, manganese and other nutrients.[26]

As hummus recipes vary, so does nutritional content, depending primarily on the relative proportions of chickpeas, tahini, and water. Hummus provides roughly 170 calories for 100 grams, and is a good to excellent (more than 10% of the Daily Value) source of dietary fiber, B vitamins, and several dietary minerals.[27][28]

Fat content, mostly from tahini and olive oil, is about 14% of the total; other major components are 65% water, 17% total carbohydrates, including a small amount of sugar, and about 10% protein.
In addition to protein and fiber, the chickpeas used in hummus are high in iron, folate, phosphorus, and B vitamins (all especially important for vegetarians and vegans who may be lacking in these nutrients). Lemon juice also contains a high level of immunity boosting vitamin C and antioxidants.
Thanks to its high fiber content, one of the benefits of hummus is that it can keep your digestive tract regular. It’s an effective and tasty way to get more fiber into your diet and fiber itself has other health benefits like helping to prevent heart disease.
