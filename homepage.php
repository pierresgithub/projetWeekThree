      <p>The earliest known recipes for a dish similar to hummus bi tahina are recorded in cookbooks written in Cairo in the 13th century.[8] Hummus has been connected to the Ayyubid Sultan, Saladin, and according to some food historians he first prepared it.[9] A cold purée of chickpeas with vinegar and pickled lemons with herbs, spices, and oil, but no tahini or garlic, appears in the Kanz al-Fawa'id fi Tanwi' al-Mawa'id;[10] and a purée of chickpeas and tahini called hummus kasa appears in the Kitab Wasf al-Atima al-Mutada: it is based on puréed chickpeas and tahini, and acidulated with vinegar (though not lemon), but it also contains many spices, herbs, and nuts, and no garlic. It is also served by rolling it out and letting it sit overnight,[11] which presumably gives it a very different texture from hummus bi tahina. Indeed, its basic ingredients—chickpeas, sesame, lemon, and garlic—have been eaten in the region for millennia.[12][13] Though chickpeas were widely eaten in the region, and they were often cooked in stews and other hot dishes,[14] puréed chickpeas eaten cold with tahini do not appear before the Abbasid period in Egypt and the Levant.
